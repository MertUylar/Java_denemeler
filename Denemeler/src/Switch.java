
public class Switch {

	public static void main(String[] args) {
		char grade='A';
		
		switch(grade) {
			case 'A':
				System.out.println("it is best grade");
				break;
			case 'B':
				System.out.println("it is not bad");
			    break;
			default:
				System.out.print("enter again");
				break;
				

	}
	}

}

public class for_and_while {

	public static void main(String[] args) {
		int b =0,c=0;
		for(int a=0;a<3;a++) {
			
			System.out.println("hello world");
			
		}
		while(b<4) {
			
			
			System.out.println("hello global");
			
			b++;
		}
		do {
			System.out.println("I love you");
			
			c++;
		}while(c<4);

	}

}
